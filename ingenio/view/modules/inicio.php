										</p>
									</div>
								</li>				
								<li>
									<div class="content">
										<h3>1924</h3>
										<p>
											En sus inicios el ingenio tenía 534 hectáreas de siembra de caña de azúcar; sin embargo, en 1924 debido a la creación del Ejido Mata Clara le son expropiadas al ingenio 170 hectáreas de tierra, por lo que el área de cultivo quedó reducida a 361 hectáreas.
											En esos años, el ingenio tenía una producción de azúcar en zafra normal de 100 a 150 toneladas, pero la reducción de tierras disminuyó notablemente la disponibilidad de materia prima.
										</p>
									</div>
								</li>
								<li>
									<div class="content">
										<h3>1927</h3>
										<p>
											En 1927 el Sr. Cristóbal Perdomo González responsable del área de campo, decidió asociarse con el Sr. Aniceto Celis del Ejido San José de En Medio, sembrando en sus parcelas cuatro hectáreas de caña de azúcar. Asimismo le proporcionaron a otros pequeños ejidatarios los recursos suficientes para la siembra de caña, con la finalidad de disponer de suficiente materia prima para alimentar el molino.
										</p>
									</div>
								</li>
								<li>
									<div class="content">
										<h3>1936</h3>
										<p>
											En 1936 se forma Azucarera Mexicana y posteriormente, en 1973, se transforma la razón social a Ingenio San José de Abajo, S. A. de C. V., hasta la actualidad, en que es manejado por la familia Perdomo Bueno, cuarta generación de Perdomo en la administración, sin que nunca hubiese sido el ingenio adquirido por el gobierno de México, por lo tanto siempre ha sido del sector privado.
										</p>
									</div>
								</li>
								<li>
									<div class="content">
										<h3>1983</h3>
										<p>
											En el año 1983 surge Grupo Perno con la participación directa de los hijos del matrimonio formado por el Ing. Rodolfo Perdomo Calatayud (QEPD) y la Sra. Adela Bueno de Perdomo; Adela, Rodolfo, Juan Fernando, Gabriela, Javier y Rosalba. Ellos asumen la responsabilidad del trabajo iniciado por su bisabuelo y continuado por las siguientes generaciones, siguiendo los pasos de su padre, quedando al frente del ingenio representando asi la más antigua participación de una familia en la industria azucarera de México, cumpliendo 100 años en la zafra 2003/2004.
										</p>
